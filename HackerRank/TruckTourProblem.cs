    public static class TruckTourProblem
    {
        //https://www.hackerrank.com/challenges/truck-tour/problem

        /* Well I assumed that the circle should always complete itself. 
         * and the solution is to find the initial point where it gives you the maximum fuel and 
         * smallest distance to the next stop. Therefore you will start with the best fuel-to-road ratio, so you will
         * not run out ouf fuel. 
         * 
         * Hackerrank has some other ideas to test idk..
         */

        public static int TruckTourHackerRank(List<List<int>> petrolpumps)
        {
            var optimalstartIndex = 0;
            var fuelInTank = 0;

            for (var i = 0; i < petrolpumps.Count; i++)
            {
                var station = petrolpumps[i];
                fuelInTank += station[0];

                if(fuelInTank > station[1])
                {
                    fuelInTank -= station[1];
                    continue;
                }

                fuelInTank = 0;
                optimalstartIndex = i + 1;                                 
            }

            return optimalstartIndex;
        }

        public static int TruckTour(List<List<int>> petrolpumps)
        {
            var optimalstartIndex = -1;
            var bestFp = -1;

            for (var i = 0; i < petrolpumps.Count; i++)
            {
                var station = petrolpumps[i];
                var sum = station[0] - station[1];

                if (sum > 0 && sum > bestFp)
                {
                    bestFp = sum;
                    optimalstartIndex = i;
                }
            }

            return optimalstartIndex;
        }

        public static int TruckTour(string stops)
        {
            List<List<int>> petrolpumps = new List<List<int>>();
            var truckLines = TruckTourStops.Split('\n');

            for (int i = 0; i < truckLines.Length; i++)
            {
                petrolpumps.Add(truckLines[i].Split(' ').ToList().Select(petrolpumpsTemp => Convert.ToInt32(petrolpumpsTemp)).ToList());
            }

            return TruckTourHackerRank(petrolpumps);
        }


